// Функция для получения цены продажи предмета
function getItemSellPrice(item) {
    // Сначала проверяем по имени в базе
    if (ITEM_SELL_PRICES[item.name]) {
        return ITEM_SELL_PRICES[item.name];
    }
    
    // Если нет в базе - рассчитываем по формуле
    const rarityMultiplier = {
        'Обычный': 1,
        'Необычный': 2,
        'Редкий': 3,
        'Эпический': 5,
        'Легендарный': 8,
        'Мифический': 12,
        'Древний': 18,
        'Божественный': 30
    };
    
    const typeModifier = {
        'weapon': 1,
        'helmet': 0.8,
        'chest': 0.8,
        'pants': 0.8,
        'boots': 0.8,
        'ring': 1.2,
        'necklace': 1.2,
        'stone': 1.2,
        'potion': 0.5,
        'mana_potion': 0.5,
        'elixir': 0.5,
        'food': 0.5,
        'scroll': 0.5,
        'battle_scroll': 0.5
    };
    
    const basePrice = 20;
    const tier = item.tier || 1;
    const rarity = rarityMultiplier[item.rarity] || 1;
    const modifier = typeModifier[item.type] || 1;
    
    let price = Math.round((tier * basePrice * rarity) * modifier);
    
    return Math.min(price, 10000);
}

// Функция для получения цены продажи ресурса
function getResourceSellPrice(resourceName) {
    if (RESOURCE_SELL_PRICES[resourceName]) {
        return RESOURCE_SELL_PRICES[resourceName];
    }
    // Ресурсы по умолчанию: 6 💰 за тир 1
    return 6;
}

// Функция для получения цены покупки предмета (магазин → игрок)
function getItemBuyPrice(item) {
    const sellPrice = getItemSellPrice(item);
    return Math.round(sellPrice * 4);
}

// Функция для получения цены покупки ресурса
function getResourceBuyPrice(resourceName) {
    const sellPrice = getResourceSellPrice(resourceName);
    return Math.round(sellPrice * 4);
}