// Функция для получения цены продажи предмета
function getItemSellPrice(item) {
    // Сначала проверяем по имени
    if (ITEM_SELL_PRICES[item.name]) {
        return ITEM_SELL_PRICES[item.name];
    }
    
    // Если нет в базе - рассчитываем на основе характеристик
    let price = 25;
    if (item.dmg) price += item.dmg * 4;
    if (item.def) price += item.def * 3;
    if (item.hp) price += Math.floor(item.hp * 1.5);
    if (item.crit) price += item.crit * 6;
    if (item.critDmg) price += item.critDmg * 2;
    if (item.dodge) price += item.dodge * 4;
    if (item.mana) price += item.mana * 2;
    if (item.value) price += Math.floor(item.value / 3);
    
    // Множитель редкости
    const rarityMultiplier = {
        'Обычный': 1,
        'Необычный': 1.5,
        'Редкий': 2,
        'Эпический': 3,
        'Легендарный': 5,
        'Мифический': 8,
        'Древний': 12,
        'Божественный': 20
    };
    price = Math.floor(price * (rarityMultiplier[item.rarity] || 1));
    
    return Math.min(price, 50000);
}